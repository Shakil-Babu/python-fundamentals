# string slicing
str = "I am shakil babu"
print(str[0:4]) # or str[:4]
# output: I am 

# from index to end
print(str[2:]) 
# output: am shakil babu


# Use negative indexes to start the slice from the end of the string:
dream = "i wanna be a great developer"
print(dream[-5:-1])
# output: lope

print(dream[-5:])
# output: loper